package com.tcs.java;

public class Data_Type {
public static void main(String[] args) {
//Byte	
System.out.println("Byte start and end range");
Byte a=-128;
System.out.println(a);
Byte b=127;
System.out.println(b);
//Short
System.out.println("Short start and end range");
short c=-32768;
System.out.println(c);
short d=32767;
System.out.println(d);
//Long
System.out.println("Long start and end range");
long e=-9223372036854775l;
System.out.println(e);
long f=9223372036854775l;
System.out.println(d);
//int
System.out.println("Integer start and end range");
int g=-2147483648;//10digits
System.out.println(g);
int h=2147483647;
System.out.println(h);
//Char
char  ch='u';
System.out.println("Character="+ch);
//float
float i=2789.78f;
System.out.println("Float="+i);
//double
double j=23980809808798.798978786;
System.out.println("Double="+j);
//boolean
boolean bo=true;
boolean boo=false;
System.out.println("Boolean value="+bo);
System.out.println("Boolean value="+boo);
//String
String first_Name="Uma";
String second_Name="Maheswari";
System.out.println(first_Name);
System.out.println(second_Name);

}
}
