package org.forloop.prac;

public class For_Increment {
	public static void main(String[] args) {
		System.out.println("For Loop Increment Example ");

		for (int i = 995; i <= 1001; i++) {
			System.out.println(i);
		}
		System.out.println("For Loop Decrement Example");
		for (int j = 2001; j >= 1996; j--)
		{
			System.out.println(j);
		}
         	}   
	}


